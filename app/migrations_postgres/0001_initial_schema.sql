CREATE INDEX idx_users_organization_status_id_desc ON users (organization_id, status, id DESC);
CREATE INDEX idx_audit_events_organization_id_desc ON audit_events (organization_id, id DESC);
CREATE INDEX idx_billing_events_organization_id_desc ON billing_events (organization_id, id DESC);
CREATE INDEX idx_usage_monthly_organization_cycle ON usage_monthly (organization_id, bucket_month);
